public class NestedLoop {

	public static void main(String[] args) {
		for (int i = 1; i <=4; i++) {
			for (int k = 1; k <= 4; k++) {

				System.out.print('i'); // write only print //print four by four 1
				System.out.print(k + ""); // bottom console
			}
			System.out.println();
		}

	}
}
